<template>
  <v-dialog width="350px" persistent v-model="editDialog">
      <v-btn accent slot="activator">
          Edit Time
      </v-btn>
      <v-card>
          <v-container>
              <v-layout row wrap>
                  <v-flex xs12>
                      <v-card-title>Edit Meetup Time</v-card-title>
                  </v-flex>
              </v-layout>
              <v-divider></v-divider>
              <v-layout row wrap>
                  <v-flex xs12>
                    {{editableTime}} | {{meetup.date}}
                    <v-time-picker v-model="editableTime" style="..." actions format="24hr">
                        <template scoped ="{save, cancel}">
                            <v-btn class="blue--text darken-1" flat @click.native="editDialog = false">Close</v-btn>
                            <v-btn class="blue--text darken-1" flat @click.native="onSaveChanges">Save</v-btn>
                        </template>
                    </v-time-picker>
                  </v-flex>
              </v-layout>
          </v-container>
      </v-card>
  </v-dialog>
</template>

<script>
export default {
  props: ['meetup'],
  data () {
    return {
      editDialog: false,
      editableTime: null
    }
  },
  methods: {
    onSaveChanges () {
      const date = new Date(this.meetup.date)
      if (typeof this.editableTime === 'string') {
        const hours = this.editableTime.match(/^(\d+)/)[1]
        const minutes = this.editableTime.match(/:(\d+)/)[1]
        date.setHours(hours)
        date.setMinutes(minutes)
      } else {
        date.setHours(this.editableTime.getHours())
        date.setMinutes(this.editableTime.getMinutes())
      }
      this.$store.dispatch('updateMeetupData', {
        id: this.meetup.id,
        date: date
      })
      this.editDialog = false
      // const newDate = this.meetup.date
      // // const hours = this.editableTime.match(/^(\d+)/)[1]
      // // const minutes = this.editableTime.match(/:(\d+)/)[1]
      // // newDate.setHours(hours)
      // // newDate.setMinutes(minutes)
      // this.$store.dispatch('updateMeetupData', {
      //   id: this.meetup.id,
      //   date: newDate
      // })
    }
  },
  mounted () {
    let str = this.meetup.date
    let date = str.split('T')
    let time = date[1].split('.')
    this.editableTime = time[0]
    // this.editableTime = new Date(this.meetup.date).toTimeString()
  }
}
</script>
